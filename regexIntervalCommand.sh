#!/bin/bash -x
#Interval Regular expressions:-
	*)These expressions tell us about the number of occurrences of a 
		character in a string. They are
==========================================================================
Expression ||			Description				||
--------------------------------------------------------------------------
{n}	   ||	Matches the preceding character appearing 'n' times 	||
	   ||	exactly							||
--------------------------------------------------------------------------
{n,m}	   ||	Matches the preceding character appearing 'n' times 	||
	   ||	but not more than m					||
--------------------------------------------------------------------------
{n,}	   ||	Matches the preceding character 			||
	   ||	only when it appears 'n' times or more			||
==========================================================================
